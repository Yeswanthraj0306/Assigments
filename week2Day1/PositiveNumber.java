package week2Day1;

public class PositiveNumber {
	public static void main(String[] args) {

		int number = 7;


		if (number > 0) {
			System.out.println("The Number "+number+" is positive ");
		}

		else {
			System.out.println("The Number "+number+" is negative");
		}

	}

}
/*Console
OUTPUT:
The Number 7 is positive
*/
