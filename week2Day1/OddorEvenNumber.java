package week2Day1;

public class OddorEvenNumber{

	public static void main(String[] args) {

		for (int i = 0; i < 20; i++) {

			if (i % 2 == 0) {
				System.out.println("The Number " +i+ " is Even Number");
			} else {
				System.out.println("The Number " +i+ " is Odd Number");

			}

		}
	}
}
